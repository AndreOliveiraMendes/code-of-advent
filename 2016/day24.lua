mazegrid = {}
number = {}
dist = {}
y = 0
for line in input:gmatch("%C+") do
    local x = 0
    mazegrid[y] = {}
    for i = 1, #line do
        if line:sub(i, i) == "#" then
            mazegrid[y][x] = "W"
        else
            if tonumber(line:sub(i, i)) then
                table.insert(number, {n = tonumber(line:sub(i, i)), x = x, y = y})
            end
            mazegrid[y][x] = (line:sub(i, i) == ".") and "P" or tonumber(line:sub(i, i))
        end
        x = x + 1
    end
    y = y + 1
end
function fg(i)
    if not dist[number[i].n] then return false end
    for j = 1, #number do
        if i ~= j then
            if not dist[number[i].n][number[j].n] then return false end
        end
    end
    return true
end
function debug()
    for i = 1, #number do
        local n = number[i].n
        for j = i + 1, #number do
            local m = number[j].n
            print(n, m, dist[n][m])
        end
    end
end
for i = 1, #number do
    local p = number[i]
    local maze = {}
    local unexplored = {{x = p.x, y = p.y}}
    local step
    repeat
        local t = {}
        step = step and (step + 1) or 0
        for _, s in pairs(unexplored) do
            if fg(i) then
                break
            end
            --maze infos
            local x, y = s.x, s.y
            if not maze[y] then maze[y] = {} end
            --check for numbers (except itself)
            if type(mazegrid[y][x]) == "number" and mazegrid[y][x] ~= p.n then
                if not dist[mazegrid[y][x]] then dist[mazegrid[y][x]] = {} end
                if not dist[p.n] then dist[p.n] = {} end
                dist[p.n][mazegrid[y][x]] = step
                dist[mazegrid[y][x]][p.n] = step
            end
            --move
            if (mazegrid[y][x - 1] and mazegrid[y][x - 1] ~= "W") and (not maze[y] or not maze[y][x - 1]) then
                maze[y][x - 1] = mazegrid[y][x - 1]
                table.insert(t, {x = x - 1, y = y})
            end
            if (mazegrid[y][x + 1] and mazegrid[y][x + 1] ~= "W") and (not maze[y] or not maze[y][x + 1]) then
                maze[y][x + 1] = mazegrid[y][x + 1]
                table.insert(t, {x = x + 1, y = y})
            end
            if (mazegrid[y - 1] and mazegrid[y - 1][x] and mazegrid[y - 1][x] ~= "W") and (not maze[y - 1] or not maze[y - 1][x]) then
                if not maze[y - 1] then maze[y - 1] = {} end
                maze[y - 1][x] = mazegrid[y - 1][x]
                table.insert(t, {x = x, y = y - 1})
            end
            if (mazegrid[y + 1] and mazegrid[y + 1][x] and mazegrid[y + 1][x] ~= "W") and (not maze[y + 1] or not maze[y + 1][x]) then
                if not maze[y + 1] then maze[y + 1] = {} end
                maze[y + 1][x] = mazegrid[y + 1][x]
                table.insert(t, {x = x, y = y + 1})
            end
        end
        unexplored = t
    until fg(i)
end
op = {}
for i, s in pairs(number) do
    if s.n ~= 0 then
        table.insert(op, s.n)
    end
end
function getminpath(op, ch, mode)
    local min
    if #op > 0 then
        for i, m in pairs(op) do
            table.remove(op, i)
            table.insert(ch, m)
            local path = getminpath(op, ch, mode)
            if not min or path < min then
                min = path
            end
            table.remove(ch, #ch)
            table.insert(op, i, m)
        end
    else
        local path, lim = 0, #ch - 1
        if mode then
            lim = lim + 1
        end
        for i = 1, lim do
            path = path + dist[ch[i]][ch[i%(#ch) + 1]]
        end
        return path
    end
    return min
end
calc = getminpath(op, {0}, false)
print("the min path is:\n" .. calc)
