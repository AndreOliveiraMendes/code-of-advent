function pml(ml, t) --Print Matrix Light
    if t then
        print("<-----(" .. t .. ")----->")
    else
        print("[-----()-----]")
    end
    for y, s in ipairs(ml) do
        print(table.concat(s, "|"))
    end
end
function gloml(ml) --get light on matrix light
    local count = 0
    for y = 1, #ml do
        for x = 1, #ml[y] do
            count = count + ml[y][x]
        end
    end
    return count
end
function f(x, y, ml)
    if ml[y] and ml[y][x] then
        return ml[y][x]
    else
        return 0
    end
end
function us(x, y, ml)
    local state, count = ml[y][x], 0
    for xr = x - 1, x + 1 do
        for yr = y - 1, y + 1 do
            if xr ~= x or yr ~= y then
                count = count + f(xr, yr, ml)
            end
        end
    end
    if state == 1 then
        if count == 2 or count == 3 then
            return 1
        else
            return 0
        end
    else
        if count == 3 then
            return 1
        else
            return 0
        end
    end
end
