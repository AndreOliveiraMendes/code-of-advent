function rotate(art) -- rotate a art anti - clockwize
    --transpose
    for x = 1, #art do
        for y = x, #art do
            art[x][y], art[y][x] = art[y][x], art[x][y]
        end
    end
    --invert column
    for x = 1, #art do
        for y = 1,#art/2 do
            art[x][y], art[x][#art + 1 - y] = art[x][#art + 1 - y], art[x][y]
        end
    end
end
function flip(art) -- flip a art
    for x = 1, #art/2 do
        for y = 1, #art do
            art[x][y], art[#art + 1 - x][y] = art[#art + 1 - x][y], art[x][y]
        end
    end
end
function artbreak(art,n) -- break a art in multiple small arts
    local max = #art/n
    local t = {}
    for i = 1, max do
        for j = 1, max do
            local nart = {}
            for x = n*(i - 1) + 1, n*i do
                local xr = (x - 1)%n + 1
                if not nart[xr] then nart[xr] = {} end
                for y = n*(j - 1) + 1, n*j do
                    local yr = (y - 1)%n + 1
                    nart[xr][yr] = art[x][y]
                end
            end
            table.insert(t,nart)
        end
    end
    return t
end
function trans(input)
    if input == 0 then
        return "."
    elseif input == 1 then
        return "#"
    elseif input == "." then
        return 0
    elseif input == "#" then
        return 1
    else
        return input
    end
end
function tosimplyfied(art) --convert a art to simplified version
    local str = ""
    for i = 1, # art do
        for j = 1, # art do
            if (j == #art) and (i < #art) then
                str = str .. trans(art[j][i]) .. "/"
            else
                str = str .. trans(art[j][i])
            end
        end
    end
    return str
end
function toart(simp) --convert simplified version to art
    local t1 = {}
    local i = 1
    local bar = string.find(simp, "/", i)
    while bar do
        local t2 = {}
        local str = string.sub(simp, i, bar - 1)
        for j = 1, #str do
            local fstr = string.sub(str, j, j)
            table.insert(t2,trans(fstr))
        end
        table.insert(t1,t2)
        i = bar + 1
        bar = string.find(simp, "/", i)
    end
    local t2 = {}
    local str = string.sub(simp, i, #simp)
    for j = 1, #str do
        local fstr = string.sub(str, j, j)
        table.insert(t2,trans(fstr))
    end
    table.insert(t1,t2)
    for x = 1, #t1 do
        for y = x, #t1 do
            t1[x][y], t1[y][x] = t1[y][x], t1[x][y]
        end
    end
    return t1
end
function artjoin(list) -- join multiples art in one
    local function tm1(t1,t2) -- merges type 1
        local t = {}
        for i = 1, #t1 do
            t[i] = {}
            for j, s in pairs(t1[i]) do
                table.insert(t[i],s)
            end
            for j, s in pairs(t2[i]) do
                table.insert(t[i],s)
            end
        end
        return t
    end
    local function tm2(t1,t2) -- merges type 2
        local t = {}
        for i = 1, #t1 + #t2 do
            if i <= #t1 then
                t[i] = t1[i]
            else
                t[i] = t2[i - #t1]
            end
        end
        return t
    end
    local max = math.sqrt(#list) -- simetry, also n^2 = #list
    local nart
    k = 0
    for i = 1, max do
        local nsart
        for j = 1, max do
            if not nsart then
                k = k + 1
                nsart = list[k]
            else
                k = k + 1
                nsart = tm1(nsart,list[k])
            end
        end
        if not nart then
            nart = nsart
        else
            nart = tm2(nart,nsart)
        end
    end
    return nart
end
function enchantaux(simp,t)
    for i, s in pairs(t) do
        if s == simp then
            return true
        end
    end
    return false
end
function action(obj,f,...)
    if #{...} > 0 then
        f(obj)
        action(obj,...)
    else
        f(obj)
    end
end
