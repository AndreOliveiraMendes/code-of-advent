function copy(art) -- copy a art (so a change made on it don't affect the original)
    local nart = {}
    for x = 1, #art do
        if not nart[x] then nart[x] = {} end
        for y = 1, #art do
            nart[x][y] = art[x][y]
        end
    end
    return nart
end
